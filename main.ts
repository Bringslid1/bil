input.onButtonPressed(Button.A, function () {
    bitbot.setLedColor(0xff0000)
    basic.pause(100)
    bitbot.goms(BBDirection.Forward, 100, 1000)
    basic.pause(100)
    bitbot.rotatems(BBRobotDirection.Left, 39, 1000)
    basic.pause(100)
    bitbot.goms(BBDirection.Forward, 100, 1000)
    basic.pause(100)
    bitbot.stop(BBStopMode.Coast)
    bitbot.setLedColor(0xFFFF00)
    basic.pause(100)
    bitbot.goms(BBDirection.Forward, 100, 1000)
    bitbot.ledRainbow(true, BBArms.Both)
})
